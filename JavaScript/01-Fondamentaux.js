//Afficher un simple message dans la console "Bonjour JavaScript"
console.log("Bonjour JavaScript");

// Les variables

// var : ancienne façon de déclarer une variable
// let : nouvelle façon de déclarer une variable
// const : pour déclarer une constante (une variable qui ne peut pas être modifiée)

let x = 4;
console.log(x);

// Les nombres
let nombre = 5;
let prix = nombre * 2.5;
console.log(prix);

// Les chaînes de caractères
let nom = "Alice";
let message = "Bonjour, " + nom + "!";
console.log(message);

// Les booléens
let estVrai = true;
let estFaux = false;
console.log(estVrai);
console.log(estFaux);

// Valeurs nulles

let y = null;
console.log(y);

//Les constantes

const PI = 3.14;
console.log(PI);
